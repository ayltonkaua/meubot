// index.js
const makeWASocket = require("@whiskeysockets/baileys").default;
const {
  useMultiFileAuthState,
  DisconnectReason,
  fetchLatestBaileysVersion
} = require("@whiskeysockets/baileys");
const { Boom } = require("@hapi/boom");
const P = require("pino");
const fs = require("fs");
require("dotenv").config();
const qrcodeTerminal = require("qrcode-terminal");

const {
  saveGasto,
  getGastosByUser,
  findOrCreateUser,
  deleteGasto,
} = require("./supabase");
const { detectarCategoria } = require("./classificador");
const { generateAccessCode, setBotSocket } = require("./auth-service");
const { getAIIntent } = require("./groq-service"); // NOVO: Importa o servi√ßo da Groq

let botSocket = null;
const processedMessages = new Set();


if (!fs.existsSync("./auth")) {
  fs.mkdirSync("./auth");
}


async function connectToWhatsApp() {
  if (botSocket) {
    console.log("üîå Encerrando conex√£o antiga...");
    try {
      botSocket.end(new Error("Reconectando..."));
    } catch (error) {
      console.log("‚ö†Ô∏è Erro ao encerrar a conex√£o antiga, mas prosseguindo.");
    }
  }

  const { state, saveCreds } = await useMultiFileAuthState("auth");

  const { version } = await fetchLatestBaileysVersion(); 
  console.log(`‚úÖ Usando vers√£o do protocolo WhatsApp Web: v${version.join('.')}`);

  botSocket = makeWASocket({
    auth: state,
    logger: P({ level: "silent" }),
    version: version,
    printQRInTerminal: false,
    browser: ["PoquidaGrana", "Desktop", "1.0.0"],
    getMessage: (key) => undefined,
  });

  botSocket.ev.on("connection.update", (update) => {
    const { connection, lastDisconnect, qr } = update;

    if (qr) {
        console.log("\nüì∏ Escaneie o QR code abaixo no WhatsApp:");
        qrcodeTerminal.generate(qr, { small: true });

        const BASE_RAILWAY_URL = process.env.RAILWAY_PUBLIC_DOMAIN || process.env.RAILWAY_STATIC_URL;
        let qrImageUrl;
        
        if (BASE_RAILWAY_URL) {
            qrImageUrl = `https://${BASE_RAILWAY_URL}/qr?data=${encodeURIComponent(qr)}`;
        } else {
            qrImageUrl = `https://${process.env.REPL_SLUG}.${process.env.REPL_OWNER}.replit.app/qr?data=${encodeURIComponent(qr)}`;
        }
        
        console.log(`\nOu acesse este link para a imagem do QR Code: ${qrImageUrl}`);
        console.log(`(Copie o link e abra no navegador para escanear ou baixar a imagem)`);
    }

    if (connection === "close") {
      const shouldReconnect =
        lastDisconnect?.error instanceof Boom &&
        lastDisconnect.error.output?.statusCode !== DisconnectReason.loggedOut;

      console.log(
        "Conex√£o fechada.",
        `Erro: ${lastDisconnect?.error?.message}.`,
        `Reconectando: ${shouldReconnect}`
      );

      if (shouldReconnect) {
        setTimeout(connectToWhatsApp, 5000);
      } else {
        console.log("‚ùå Desconectado permanentemente. N√£o ser√° poss√≠vel reconectar.");
      }
    } else if (connection === "open") {
      console.log("‚úÖ Conex√£o estabelecida com o WhatsApp!");
      console.log(`üë§ Usu√°rio conectado: ${botSocket.user?.name || "Desconhecido"}`);
      console.log(`üì± N√∫mero do bot: ${botSocket.user?.id || "N/A"}`);
      
      setBotSocket(botSocket);
      
      setTimeout(async () => {
        console.log("üîß Socket configurado e pronto para envio de c√≥digos");
        try {
          console.log("üîç Testando conectividade do bot...");
          await botSocket.sendMessage(botSocket.user.id, {
            text: "ü§ñ Bot online e pronto para enviar c√≥digos!",
          });
          console.log("‚úÖ Teste de conectividade realizado com sucesso");
        } catch (testError) {
          console.error("‚ö†Ô∏è Aviso: Poss√≠vel problema de conectividade:", testError.message);
        }
      }, 5000);
    }
  });

  botSocket.ev.on("creds.update", saveCreds);

  botSocket.ev.on("messages.upsert", async ({ messages }) => {
    const msg = messages[0];
    if (!msg.message || !msg.key.id) return;

    if (msg.key.fromMe) {
      console.log("ü§ñ Ignorando mensagem enviada pelo pr√≥prio bot");
      return;
    }

    const messageId = msg.key.id;
    if (processedMessages.has(messageId)) {
      console.log(`üö´ Mensagem duplicada ignorada: ${messageId}`);
      return;
    }
    processedMessages.add(messageId);
    setTimeout(() => processedMessages.delete(messageId), 60000);

    const sender = msg.key.remoteJid;
    let text = "";

    if (msg.message.conversation) {
      text = msg.message.conversation;
    } else if (msg.message.extendedTextMessage?.text) {
      text = msg.message.extendedTextMessage.text;
    } else if (msg.message.imageMessage?.caption) {
      text = msg.message.imageMessage.caption;
    }

    if (!text) return;
    // O Groq far√° a normaliza√ß√£o, ent√£o podemos enviar o texto original ou a vers√£o processada
    // text = text.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, ""); 

    console.log(`üì© Processando: "${text}" de ${sender}`);

    const usuario = await findOrCreateUser(sender);
    if (!usuario) {
        console.log(`‚ùå N√£o foi poss√≠vel encontrar ou criar o usu√°rio ${sender}. Abortando processamento.`);
        await botSocket.sendMessage(sender, { text: "‚ö†Ô∏è Ocorreu um erro ao identificar seu usu√°rio. Por favor, tente novamente mais tarde." });
        return;
    }

    // --- NOVO: L√≥gica principal com a API da Groq ---
    let intent = 'outro';
    let entities = {};

    // Prioriza clique em lista, depois a IA
    if (msg.message.listResponseMessage) {
        const selectedRowId = msg.message.listResponseMessage.singleSelectReply.selectedRowId;
        console.log(`Item de lista clicado: ${selectedRowId}`);
        // Mapeie os IDs da lista para inten√ß√µes e entidades para reuso da l√≥gica abaixo
        switch (selectedRowId) {
            case 'id_historico_list': intent = 'ver_historico'; break;
            case 'id_relatorio_list': intent = 'ver_relatorio_web'; break;
            case 'id_excluir_gasto_list': intent = 'excluir_gasto'; break;
            // Para a ajuda, talvez chame uma inten√ß√£o 'ajuda'
            case 'id_ajuda_list': intent = 'ajuda'; break; 
            default: intent = 'outro'; break;
        }
        // Se a intera√ß√£o veio de um bot√£o, n√£o precisamos da Groq para essa inten√ß√£o
    } else {
        // Se n√£o for um clique em lista, chama a Groq API
        const groqResponse = await getAIIntent(text);
        intent = groqResponse.intent;
        entities = groqResponse.entities;
        console.log(`Groq AI - Inten√ß√£o: ${intent}, Entidades: ${JSON.stringify(entities)}`);
    }

    switch (intent) {
        case 'registrar_gasto': {
            const valor = entities.valor;
            // Se a Groq n√£o forneceu a categoria, tente detectar com seu classificador existente
            const categoria = entities.categoria || detectarCategoria(text); 
            const descricao = entities.descricao || text; // Use a descri√ß√£o da Groq ou a mensagem original

            if (valor && categoria) {
                const gastoParaSalvar = {
                    usuario_id: sender,
                    valor: parseFloat(valor), // Garante que √© um n√∫mero
                    categoria: categoria,
                    descricao: descricao,
                };
                await saveGasto(gastoParaSalvar);

                const confirmSections = [
                    {
                        title: "Pr√≥ximos Passos",
                        rows: [
                            { id: 'id_excluir_gasto_list', title: 'üóëÔ∏è Excluir √öltimo Gasto', description: 'Remover o gasto que acabei de registrar' },
                            { id: 'id_historico_list', title: 'üìú Ver Hist√≥rico', description: 'Consultar meus gastos anteriores' },
                            { id: 'id_relatorio_list', title: 'üìä Acessar Relat√≥rio Web', description: 'Ver gr√°ficos e estat√≠sticas' }
                        ]
                    }
                ];

                const confirmListMessage = {
                    text: `‚úÖ *Gasto Registrado!*\n\nüí∞ Valor: R$ ${parseFloat(valor).toFixed(2)}\nüìÇ Categoria: ${categoria}\n\nO que voc√™ gostaria de fazer a seguir?`,
                    footer: 'Escolha uma op√ß√£o na lista:',
                    title: "A√ß√µes do Gasto",
                    buttonText: "Ver A√ß√µes",
                    sections: confirmSections
                };
                await botSocket.sendMessage(sender, confirmListMessage);

            } else {
                await botSocket.sendMessage(sender, { text: `Entendi que voc√™ quer registrar um gasto, mas preciso do *valor* e da *categoria*. Ex: "Gastei 50 no almo√ßo".` });
            }
            break;
        }

        case 'ver_historico': {
            const gastos = await getGastosByUser(sender);
            if (gastos.length === 0) {
                await botSocket.sendMessage(sender, { text: "Voc√™ ainda n√£o tem gastos registrados." });
                return;
            }
            let mensagemHistorico = "üìä *Seu Hist√≥rico de Gastos (√öltimos 10):*\n\n";
            let totalGastosExibidos = 0;
            const ultimosGastos = gastos.slice(0, 10);

            ultimosGastos.forEach(gasto => {
                const data = new Date(gasto.criado_em).toLocaleDateString('pt-BR');
                mensagemHistorico += `‚Ä¢ ${data} - R$ ${parseFloat(gasto.valor).toFixed(2)} (${gasto.categoria})\n`;
                totalGastosExibidos += parseFloat(gasto.valor);
            });

            mensagemHistorico += `\n*Total exibido: R$ ${totalGastosExibidos.toFixed(2)}*`;
            mensagemHistorico += `\n\nPara ver o relat√≥rio completo: */codigo*`;
            await botSocket.sendMessage(sender, { text: mensagemHistorico });
            break;
        }

        case 'ver_relatorio_web':
        case 'obter_codigo_acesso': { // Unifica a l√≥gica para acessar o painel
            const accessCode = generateAccessCode(sender);
            const BASE_RAILWAY_URL = process.env.RAILWAY_PUBLIC_DOMAIN || process.env.RAILWAY_STATIC_URL;
            const webUrl = BASE_RAILWAY_URL ? `https://${BASE_RAILWAY_URL}` : `https://${process.env.REPL_SLUG}.${process.env.REPL_OWNER}.replit.app`;
            
            let responseText;
            if (intent === 'ver_relatorio_web') {
                responseText = `üìä *Acesse seu relat√≥rio completo*\n\nPara ver gr√°ficos e estat√≠sticas detalhadas, digite: */codigo*\n\nOu acesse diretamente: ${webUrl}`;
            } else { // obter_codigo_acesso
                responseText = `üîê *C√≥digo de Acesso ao Sistema Web*\n\nSeu c√≥digo: *${accessCode}*\n\nAcesse: ${webUrl}\n\n‚è∞ Este c√≥digo expira em 10 minutos.`;
            }
            await botSocket.sendMessage(sender, { text: responseText });
            break;
        }

        case 'excluir_gasto': {
            const userGastos = await getGastosByUser(sender);
            if (userGastos.length === 0) {
                await botSocket.sendMessage(sender, { text: "Voc√™ n√£o tem gastos registrados para excluir." });
                return;
            }
            const ultimoGasto = userGastos[0];
            
            const deleteResult = await deleteGasto(ultimoGasto.id);

            if (deleteResult.success) {
                await botSocket.sendMessage(sender, {
                    text: `üóëÔ∏è *Gasto Exclu√≠do!*\n\nO √∫ltimo gasto (R$ ${parseFloat(ultimoGasto.valor).toFixed(2)} - ${ultimoGasto.categoria}) foi removido com sucesso.`
                });
            } else {
                await botSocket.sendMessage(sender, {
                    text: `‚ùå Erro ao excluir o gasto: ${deleteResult.error}`
                });
            }
            break;
        }
        
        case 'saudacao': {
            await botSocket.sendMessage(sender, { text: "üëã Ol√°! Eu sou o PoquidaGrana, seu assistente financeiro. Como posso te ajudar hoje?" });
            break;
        }
        
        case 'agradecimento': {
            await botSocket.sendMessage(sender, { text: "De nada! Fico feliz em ajudar com suas finan√ßas." });
            break;
        }

        case 'ajuda':
        case 'outro': // Se a IA n√£o entender, ou for uma inten√ß√£o 'ajuda' expl√≠cita
        default: {
            console.log("‚ö†Ô∏è Inten√ß√£o n√£o reconhecida ou comando de ajuda. Oferecendo menu.");
            const helpSections = [
              {
                title: "Op√ß√µes R√°pidas",
                rows: [
                  { id: 'id_historico_list', title: "üìú Ver Hist√≥rico", description: "Veja seus √∫ltimos gastos" },
                  { id: 'id_relatorio_list', title: "üìä Acessar Relat√≥rio Web", description: "Abra o painel de controle" },
                  { id: 'id_excluir_gasto_list', title: 'üóëÔ∏è Excluir √öltimo Gasto', description: 'Remover o gasto mais recente' }
                ],
              },
            ];

            const helpListMessage = {
              text: `‚ùì *N√£o entendi muito bem. Voc√™ pode me dizer o que gostaria de fazer (ex: "Gastei 15 no almo√ßo") ou escolher uma op√ß√£o:*\n`,
              footer: 'Escolha uma op√ß√£o na lista:',
              title: "Ajuda e Comandos",
              buttonText: "Ver Op√ß√µes",
              sections: helpSections,
            };

            await botSocket.sendMessage(sender, helpListMessage);
            break;
        }
    }
  });
}

